"""Track -- состояние трека между тактами (Value Object, гл.4-бис §4.4, гл.5 §5.7).

Патент: «Летит -- не из куба» (§4-бис.4) -- окно быстрого времени слишком коротко
для доплера, движение цели определяется трекингом выброса МЕЖДУ тактами, а не
внутри одного куба. §5.7: причинностная группа (цель+гребёнка) едет по дальности
и/или углу от такта к такту, её ведут трекингом; правило переднего края -- потактовое
(гл.5), скорость/«летит» -- задача этого модуля, не арбитра.

`Track` -- иммутабельный снапшот одного трека, отдаваемый наружу `Tracker.update()`
после обработки такта. Только примитивы (кросс-язычно под C++/msgpack, как
`TargetDecision` в `core/models/tokenizer/arbiter.py`).
"""
from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class Track:
    """Снапшот трека после очередного такта.

    `kx`, `ky`, `lead_r` -- последнее ИЗВЕСТНОЕ положение (последняя сматченная
    декларация; при пропуске такта не меняется, см. `NearestNeighborTracker`).
    `history` -- кортеж `(tact, kx, ky, lead_r)` только по тактам с УСПЕШНОЙ
    ассоциацией (пропуски в history не пишутся -- скорость считается по реально
    наблюдённым точкам).
    `vel_r` -- скорость по дальности (бин/такт, знак: отрицательная = сближение,
    убывает `lead_r`), сглаженная линейной регрессией по `history` (МНК-наклон
    `lead_r(tact)`) -- устойчивее к джиттеру одиночной пары точек, чем разность
    последних двух отсчётов.
    `vel_angle` -- модуль угловой скорости, `sqrt(slope_kx^2 + slope_ky^2)`
    (МНК-наклоны `kx(tact)`, `ky(tact)`).
    `age` -- сколько тактов трек живёт (считая и пропуски).
    `missed` -- сколько тактов ПОДРЯД без ассоциации (сбрасывается в 0 при матче).
    `is_moving` -- «летит» (§4-бис.4): `sqrt(vel_r^2 + vel_angle^2) > moving_threshold`.
    """

    track_id: int
    kx: float
    ky: float
    lead_r: int
    history: tuple[tuple[int, float, float, int], ...]
    vel_r: float
    vel_angle: float
    age: int
    missed: int
    is_moving: bool
